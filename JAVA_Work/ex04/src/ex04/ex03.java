package ex04;

public class ex03 {
	public static void main(String[] args) {
		int num1 = 10;

		System.out.println(++num1);
		System.out.println(num1++);

		System.out.println(num1);

		System.out.println(num1);
		System.out.println(num1--);

		System.out.println(--num1);

		for (int i = 1; i < 11; i++) {
			System.out.println("제"+i+"의아해도무섭다고그리오.");
		}
	}
}
